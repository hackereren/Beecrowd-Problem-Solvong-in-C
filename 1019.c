#include<stdio.h>

int main()
{
    int N,a,b,c,d,e;
    scanf("%d",&N);

    if(N>=3600){
    a=N/3600;
    }
    else{
    a=0;
    }
    b=N%3600;

    if(b>60){
    c=b/60;
    }
    else{
    c=0;
    }
    d=b%60;

    if(d<60 ){
    e=d;
    }
    else{
    e=0;
    }
    printf("%d:%d:%d\n",a,c,e);

    return 0;
}
